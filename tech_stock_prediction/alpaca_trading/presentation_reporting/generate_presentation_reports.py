"""
Generate the unified presentation report - exactly 5 standard plots per
universe (+ 1 optional model-interpretation plot) + comparison plots/CSV +
validation/summary/slide-plan markdown - for one or all four stock
universes.

Usage:
    python alpaca_trading/presentation_reporting/generate_presentation_reports.py
    python alpaca_trading/presentation_reporting/generate_presentation_reports.py --universe original_tech

This script only reads existing result/backtest/Alpaca log files (see
presentation_data_loader.py). It does not train any model, does not run or
change any existing training or trading pipeline, and does not modify or
delete any existing log, model, or data file. It only writes into
alpaca_trading/presentation_reporting/output/.

Every universe gets exactly the same 5 named plots, generated by exactly
the same code path:
    01_backtest_vs_benchmark.png
    02_top_k_results.png
    03_hourly_alpaca_vs_benchmark.png
    04_daily_alpaca_vs_benchmark.png
    05_signal_selection_analysis.png
plus one optional model-interpretation plot:
    06_signal_probability_distribution.png
If real data is missing or too thin, the plot is replaced by a clearly
labeled status graphic - never by an estimated or fabricated value.
"""

from pathlib import Path
import argparse
import sys

REPORTING_ROOT = Path(__file__).resolve().parent
sys.path.insert(0, str(REPORTING_ROOT))

import pandas as pd  # noqa: E402

import presentation_data_loader as loader  # noqa: E402
import presentation_metrics as metrics  # noqa: E402
import presentation_plots as plots  # noqa: E402
import reporting_config as cfg  # noqa: E402

REQUIRED_PLOT_FILES = [
    "01_backtest_vs_benchmark.png",
    "02_top_k_results.png",
    "03_hourly_alpaca_vs_benchmark.png",
    "04_daily_alpaca_vs_benchmark.png",
    "05_signal_selection_analysis.png",
]
OPTIONAL_PLOT_FILES = ["06_signal_probability_distribution.png"]


def build_universe_report(universe_name):
    benchmark_ticker = cfg.BENCHMARKS[universe_name]
    universe_title = cfg.UNIVERSE_TITLE_DE[universe_name]
    universe_tickers = cfg.UNIVERSES[universe_name]
    output_dir = cfg.universe_output_dir(universe_name)

    result = {
        "universe": universe_name,
        "title": universe_title,
        "benchmark": benchmark_ticker,
        "research_question": cfg.RESEARCH_QUESTION_DE[universe_name],
        "sources": {},
        "backtest_summary": None,
        "top_k_summary": None,
        "hourly_alpaca_summary": None,
        "daily_alpaca_summary": None,
        "daily_alpaca_orders_summary": None,
        "signal_summary": None,
        "signal_aggregate": None,
        "plots_present": {},
    }

    # ---- Plot 1: 01_backtest_vs_benchmark.png -------------------------------
    backtest_ds = loader.load_backtest_predictions(universe_name)
    result["sources"]["backtest"] = backtest_ds.to_dict()
    plot1_path = output_dir / "01_backtest_vs_benchmark.png"

    best_top_k = None
    daily = None

    if not backtest_ds.is_usable:
        plots.plot_status_message(
            title=f"Historischer Backtest - {universe_title} vs. {benchmark_ticker}",
            status=backtest_ds.status,
            message_lines=[backtest_ds.note],
            output_path=plot1_path,
        )
    else:
        best_top_k, candidates = metrics.select_best_top_k(backtest_ds.source_path)
        if best_top_k is None:
            best_top_k = max(
                candidates,
                key=lambda k: metrics.total_return_from_returns(
                    metrics.reconstruct_daily_topk_backtest(backtest_ds.data, benchmark_ticker, k)["Strategy_Return"]
                ),
            )
        daily = metrics.reconstruct_daily_topk_backtest(backtest_ds.data, benchmark_ticker, best_top_k)
        summary = metrics.summarize_backtest(daily, benchmark_ticker)
        summary["top_k"] = best_top_k
        result["backtest_summary"] = summary

        strategy_index = metrics.build_cumulative_index(daily["Strategy_Return"])
        benchmark_index = metrics.build_cumulative_index(daily["Benchmark_Return"])

        metric_lines = [
            f"Strategie-Rendite: {summary['strategy_return_net']:+.1%}",
            f"Benchmark-Rendite ({benchmark_ticker}): {summary['benchmark_return']:+.1%}",
            f"Outperformance: {summary['difference']:+.1%}",
            f"Sharpe: {summary['sharpe_ratio']:.2f}",
            f"MaxDD: {summary['max_drawdown']:.1%}",
            f"Zeitraum: {summary['period_start'].date()} - {summary['period_end'].date()}",
        ]

        plots.plot_cumulative_lines(
            title=f"Historischer Backtest - {universe_title} vs. {benchmark_ticker}",
            subtitle=(
                f"Top-{best_top_k} Outperformance-LSTM, "
                f"{daily.index.min().date()} bis {daily.index.max().date()} ({len(daily)} Handelstage)"
            ),
            dates=daily.index,
            strategy_index=strategy_index,
            benchmark_index=benchmark_index,
            strategy_label=f"Top-{best_top_k} Strategie",
            benchmark_label=f"{benchmark_ticker} (Index)",
            output_path=plot1_path,
            metric_lines=metric_lines,
            preliminary=(backtest_ds.status == cfg.STATUS_PRELIMINARY),
        )
    result["plots_present"]["01_backtest_vs_benchmark.png"] = plot1_path.exists()

    # ---- Plot 2: 02_top_k_results.png ---------------------------------------
    plot2_path = output_dir / "02_top_k_results.png"
    top_k_table = metrics.load_top_k_table(backtest_ds.source_path) if backtest_ds.is_usable else None
    result["sources"]["top_k"] = {
        "status": cfg.STATUS_READY if top_k_table is not None else cfg.STATUS_MISSING,
        "source_path": backtest_ds.source_path,
        "note": "" if top_k_table is not None else f"Keine Top-K-Ergebnisdatei gefunden. {backtest_ds.note}",
        "observations": int(len(top_k_table)) if top_k_table is not None else 0,
    }

    if top_k_table is None:
        plots.plot_status_message(
            title=f"Top-K-Analyse - {universe_title}",
            status=cfg.STATUS_MISSING,
            message_lines=[
                "Keine Top-K-Ergebnisdatei (lstm_outperformance_top_k_results.csv) gefunden.",
                backtest_ds.note,
            ],
            output_path=plot2_path,
        )
    else:
        best_row = top_k_table.sort_values("strategy_return", ascending=False).iloc[0]
        result["top_k_summary"] = {
            "table": top_k_table.to_dict(orient="records"),
            "best_top_k": int(best_row["top_k"]),
            "best_strategy_return": float(best_row["strategy_return"]),
        }
        metric_lines = [
            f"Bestes Top-K: Top-{int(best_row['top_k'])} ({best_row['strategy_return']:+.1%})",
            f"Zeitraum: {top_k_table['test_start'].iloc[0]} - {top_k_table['test_end'].iloc[0]}",
        ]
        plots.plot_top_k_results(
            title=f"Top-K-Analyse - {universe_title}",
            subtitle="Top-1 bis Top-5 vs. Buy-and-Hold der Universums-Aktien (nicht der Benchmark-Index)",
            top_k_values=top_k_table["top_k"].tolist(),
            strategy_returns=top_k_table["strategy_return"].tolist(),
            buy_and_hold_returns=top_k_table["buy_and_hold_return"].tolist(),
            differences=top_k_table["difference"].tolist(),
            average_positions=top_k_table["average_number_of_positions"].tolist(),
            output_path=plot2_path,
            metric_lines=metric_lines,
            preliminary=(backtest_ds.status == cfg.STATUS_PRELIMINARY),
        )
    result["plots_present"]["02_top_k_results.png"] = plot2_path.exists()

    # ---- Plot 3: 03_hourly_alpaca_vs_benchmark.png --------------------------
    hourly_ds = loader.load_hourly_alpaca_performance(universe_name)
    result["sources"]["hourly_alpaca"] = hourly_ds.to_dict()
    plot3_path = output_dir / "03_hourly_alpaca_vs_benchmark.png"

    if not hourly_ds.is_usable:
        plots.plot_status_message(
            title=f"Hourly Paper Trading - {universe_title} vs. {benchmark_ticker}",
            status=hourly_ds.status,
            message_lines=[cfg.INSUFFICIENT_HOURLY_MESSAGE, hourly_ds.note],
            output_path=plot3_path,
        )
    else:
        # Defensive branch: not currently reachable with real data (no clean
        # per-universe hourly index series exists yet), kept so a future
        # hourly dataset in the expected portfolio_index/benchmark_index
        # shape renders automatically instead of silently staying MISSING.
        data = hourly_ds.data
        has_index_columns = {"portfolio_index", "benchmark_index", "timestamp"}.issubset(data.columns)
        if not has_index_columns:
            plots.plot_status_message(
                title=f"Hourly Paper Trading - {universe_title} vs. {benchmark_ticker}",
                status=hourly_ds.status,
                message_lines=[
                    "Hourly-Zeilen gefunden, aber nicht im erwarteten Index-Format - keine belastbare Kurve moeglich.",
                    hourly_ds.note,
                ],
                output_path=plot3_path,
            )
        else:
            summary = metrics.summarize_alpaca_performance(data, date_column="timestamp")
            result["hourly_alpaca_summary"] = summary
            metric_lines = [
                f"n={summary['observations']} Zeitstempel",
                f"Portfolio-Rendite: {summary['portfolio_return']:+.1%}",
                f"Benchmark-Rendite ({benchmark_ticker}): {summary['benchmark_return']:+.1%}",
                f"Outperformance: {summary['difference']:+.1%}",
                f"MaxDD: {summary['max_drawdown']:.1%}",
            ]
            plots.plot_cumulative_lines(
                title=f"Hourly Paper Trading - {universe_title} vs. {benchmark_ticker}",
                subtitle=f"{summary['observations']} Beobachtungen",
                dates=data["timestamp"],
                strategy_index=data["portfolio_index"],
                benchmark_index=data["benchmark_index"],
                strategy_label="Portfolio",
                benchmark_label=f"{benchmark_ticker} (Index)",
                output_path=plot3_path,
                metric_lines=metric_lines,
                preliminary=(hourly_ds.status == cfg.STATUS_PRELIMINARY),
            )
    result["plots_present"]["03_hourly_alpaca_vs_benchmark.png"] = plot3_path.exists()

    # ---- Plot 4: 04_daily_alpaca_vs_benchmark.png ---------------------------
    daily_alpaca_ds = loader.load_daily_alpaca_performance(universe_name)
    result["sources"]["daily_alpaca"] = daily_alpaca_ds.to_dict()
    plot4_path = output_dir / "04_daily_alpaca_vs_benchmark.png"

    orders_ds = loader.load_daily_alpaca_orders(universe_name)
    result["sources"]["daily_alpaca_orders"] = orders_ds.to_dict()
    order_summary = metrics.summarize_orders(orders_ds.data) if orders_ds.is_usable else {
        "buys": None, "sells": None, "total_trades": None, "total_notional_traded": None,
    }
    result["daily_alpaca_orders_summary"] = order_summary

    if not daily_alpaca_ds.is_usable:
        plots.plot_status_message(
            title=f"Daily Paper Trading - {universe_title} vs. {benchmark_ticker}",
            status=daily_alpaca_ds.status,
            message_lines=[cfg.INSUFFICIENT_DAILY_MESSAGE, daily_alpaca_ds.note],
            output_path=plot4_path,
        )
    else:
        data = daily_alpaca_ds.data
        summary = metrics.summarize_alpaca_performance(data)
        result["daily_alpaca_summary"] = summary

        metric_lines = [
            f"Zeitraum: {summary['period_start'].date()} - {summary['period_end'].date()} (n={summary['observations']})",
            f"Portfolio-Rendite: {summary['portfolio_return']:+.1%}",
            f"Benchmark-Rendite ({benchmark_ticker}): {summary['benchmark_return']:+.1%}",
            f"Outperformance: {summary['difference']:+.1%}",
            f"MaxDD: {summary['max_drawdown']:.1%}",
            f"Trades: {order_summary['total_trades']} (Kauf {order_summary['buys']}, Verkauf {order_summary['sells']})",
        ]
        if order_summary.get("total_notional_traded") is not None:
            metric_lines.append(f"Gehandeltes Volumen: ${order_summary['total_notional_traded']:,.0f}")

        plots.plot_cumulative_lines(
            title=f"Daily Paper Trading - {universe_title} vs. {benchmark_ticker}",
            subtitle=f"Isolierte Tages-Simulation je Universum, {summary['observations']} Beobachtungen",
            dates=data["date"],
            strategy_index=data["portfolio_index"],
            benchmark_index=data["benchmark_index"],
            strategy_label="Portfolio (Top-3)",
            benchmark_label=f"{benchmark_ticker} (Index)",
            output_path=plot4_path,
            metric_lines=metric_lines,
            preliminary=(daily_alpaca_ds.status == cfg.STATUS_PRELIMINARY),
        )
    result["plots_present"]["04_daily_alpaca_vs_benchmark.png"] = plot4_path.exists()

    # ---- Plot 5: 05_signal_selection_analysis.png ---------------------------
    signal_ds = loader.load_signal_history(universe_name, timeframe="1Day")
    result["sources"]["signal_history"] = signal_ds.to_dict()
    plot5_path = output_dir / "05_signal_selection_analysis.png"

    if not signal_ds.is_usable:
        plots.plot_status_message(
            title=f"Signalanalyse - {universe_title}",
            status=signal_ds.status,
            message_lines=[signal_ds.note],
            output_path=plot5_path,
        )
    else:
        order_counts = metrics.per_ticker_order_counts(orders_ds.data) if orders_ds.is_usable else {}
        selection_df, distinct_dates = metrics.summarize_signal_selection(
            signal_ds.data, universe_tickers, order_counts=order_counts,
        )
        aggregate = metrics.summarize_signal_aggregate(signal_ds.data, selection_df, distinct_dates)
        result["signal_summary"] = {"distinct_dates": distinct_dates, "table": selection_df.to_dict(orient="records")}
        result["signal_aggregate"] = aggregate

        def _format_selection_row(row):
            if pd.isna(row["average_probability"]):
                return "keine Signale"
            holding_duration = (
                f"{row['average_holding_duration']:.1f}T" if pd.notna(row["average_holding_duration"]) else "n/a"
            )
            return (
                f"Ø p={row['average_probability']:.2f}, Ø Rang={row['average_rank']:.1f}, "
                f"Kauf {int(row['buys'])}/Verkauf {int(row['sells'])}, "
                f"Ø Haltedauer {holding_duration}"
            )

        secondary_annotations = [_format_selection_row(row) for _, row in selection_df.iterrows()]

        footer_lines = [
            f"n={distinct_dates} Signaltage",
            f"Top-1 am haeufigsten: {aggregate['most_frequent_top1'] or 'n/a'}",
            f"Top-3 am haeufigsten: {', '.join(aggregate['most_frequent_top3']) if aggregate['most_frequent_top3'] else 'n/a'}",
            (
                f"Ø Probability: {aggregate['average_probability_overall']:.2f}"
                if pd.notna(aggregate["average_probability_overall"])
                else "Ø Probability: n/a"
            ),
            (
                f"Ø Umschichtung/Tag: {aggregate['average_turnover']:.1f}"
                if aggregate["average_turnover"] is not None
                else "Ø Umschichtung/Tag: n/a"
            ),
        ]

        plots.plot_horizontal_bars(
            title=f"Signalanalyse - {universe_title}",
            subtitle=f"Anteil der Signaltage in Top-K, n={distinct_dates} Tage (Daily Alpaca-Signale)",
            labels=selection_df["ticker"],
            values=selection_df["selection_share"],
            value_format=lambda value: f"{value:.0%}",
            output_path=plot5_path,
            value_label="Anteil der Signaltage in Top-K",
            metric_lines=footer_lines,
            preliminary=(signal_ds.status == cfg.STATUS_PRELIMINARY),
            secondary_annotations=secondary_annotations,
        )
    result["plots_present"]["05_signal_selection_analysis.png"] = plot5_path.exists()

    # ---- Plot 6 (optional, model interpretation only): ---------------------
    #      06_signal_probability_distribution.png
    plot6_path = output_dir / "06_signal_probability_distribution.png"
    if not signal_ds.is_usable:
        plots.plot_status_message(
            title=f"Wahrscheinlichkeitsverteilung - {universe_title}",
            status=signal_ds.status,
            message_lines=[signal_ds.note],
            output_path=plot6_path,
        )
    else:
        probabilities = signal_ds.data["probability"].dropna()
        metric_lines = [
            f"n={len(probabilities)} Vorhersagen",
            f"Ø={probabilities.mean():.2f}, Median={probabilities.median():.2f}",
        ]
        plots.plot_signal_probability_distribution(
            title=f"Wahrscheinlichkeitsverteilung - {universe_title}",
            subtitle="Optional: Modellinterpretation, nicht Teil der 5 Pflichtplots",
            probabilities=probabilities,
            output_path=plot6_path,
            metric_lines=metric_lines,
            preliminary=(signal_ds.status == cfg.STATUS_PRELIMINARY),
        )
    result["plots_present"]["06_signal_probability_distribution.png"] = plot6_path.exists()

    return result


def build_comparison_outputs(universe_results):
    cfg.COMPARISON_OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    universes = [r["universe"] for r in universe_results]
    universe_titles = [r["title"] for r in universe_results]

    # ---- 01_backtest_comparison.png -----------------------------------------
    backtest_values, backtest_statuses = [], []
    for r in universe_results:
        if r["backtest_summary"] is None:
            backtest_values.append(None)
            backtest_statuses.append(cfg.STATUS_MISSING)
        else:
            backtest_values.append(r["backtest_summary"]["difference"])
            backtest_statuses.append(r["sources"]["backtest"]["status"])

    plots.plot_comparison_bars(
        title="Backtest-Outperformance je Universum",
        subtitle="Differenz Top-K-Strategie minus Benchmark (Zeitraeume koennen je Universum abweichen)",
        universes=universe_titles,
        values=backtest_values,
        statuses=backtest_statuses,
        output_path=cfg.COMPARISON_OUTPUT_DIR / "01_backtest_comparison.png",
    )

    # ---- 02_hourly_comparison.png -------------------------------------------
    hourly_values, hourly_statuses = [], []
    for r in universe_results:
        if r["hourly_alpaca_summary"] is None:
            hourly_values.append(None)
            hourly_statuses.append(cfg.STATUS_MISSING)
        else:
            hourly_values.append(r["hourly_alpaca_summary"]["difference"])
            hourly_statuses.append(r["sources"]["hourly_alpaca"]["status"])

    plots.plot_comparison_bars(
        title="Hourly-Alpaca-Outperformance je Universum",
        subtitle="Keine belastbare universenspezifische Attribution ueber die Zeit verfuegbar",
        universes=universe_titles,
        values=hourly_values,
        statuses=hourly_statuses,
        output_path=cfg.COMPARISON_OUTPUT_DIR / "02_hourly_comparison.png",
    )

    # ---- 03_daily_comparison.png --------------------------------------------
    daily_values, daily_statuses = [], []
    for r in universe_results:
        if r["daily_alpaca_summary"] is None:
            daily_values.append(None)
            daily_statuses.append(cfg.STATUS_MISSING)
        else:
            daily_values.append(r["daily_alpaca_summary"]["difference"])
            daily_statuses.append(r["sources"]["daily_alpaca"]["status"])

    plots.plot_comparison_bars(
        title="Daily-Alpaca-Outperformance je Universum",
        subtitle="Isolierte Tages-Simulation, wenige Handelstage - vorlaeufig",
        universes=universe_titles,
        values=daily_values,
        statuses=daily_statuses,
        output_path=cfg.COMPARISON_OUTPUT_DIR / "03_daily_comparison.png",
    )

    # ---- 04_signal_comparison.png -------------------------------------------
    signal_values, signal_statuses = [], []
    for r in universe_results:
        table = (r.get("signal_summary") or {}).get("table")
        if not table:
            signal_values.append(None)
            signal_statuses.append(cfg.STATUS_MISSING)
        else:
            top_row = max(table, key=lambda row: row["selection_share"])
            signal_values.append(top_row["selection_share"])
            signal_statuses.append(r["sources"]["signal_history"]["status"])

    plots.plot_comparison_bars(
        title="Signalkonzentration je Universum",
        subtitle="Anteil des meistgewaehlten Tickers an allen Signaltagen (Top-1 nach Haeufigkeit)",
        universes=universe_titles,
        values=signal_values,
        statuses=signal_statuses,
        output_path=cfg.COMPARISON_OUTPUT_DIR / "04_signal_comparison.png",
        value_label="Anteil Signaltage (Top-1-Ticker)",
        value_is_fraction=True,
    )

    # ---- 05_final_universe_comparison.png -----------------------------------
    plots.plot_final_comparison(
        universes=universe_titles,
        backtest_values=backtest_values,
        hourly_values=hourly_values,
        daily_values=daily_values,
        statuses_by_metric={"backtest": backtest_statuses, "hourly": hourly_statuses, "daily": daily_statuses},
        output_path=cfg.COMPARISON_OUTPUT_DIR / "05_final_universe_comparison.png",
        title="Gesamtvergleich: Backtest vs. Hourly Alpaca vs. Daily Alpaca",
    )

    rows = []
    for r in universe_results:
        backtest_summary = r["backtest_summary"] or {}
        hourly_summary = r["hourly_alpaca_summary"] or {}
        daily_summary = r["daily_alpaca_summary"] or {}
        order_summary = r.get("daily_alpaca_orders_summary") or {}
        aggregate = r.get("signal_aggregate") or {}

        data_quality_flags = []
        if r["sources"]["backtest"]["status"] == cfg.STATUS_MISSING:
            data_quality_flags.append("backtest_missing")
        if r["sources"]["top_k"]["status"] == cfg.STATUS_MISSING:
            data_quality_flags.append("top_k_missing")
        if r["sources"]["hourly_alpaca"]["status"] == cfg.STATUS_MISSING:
            data_quality_flags.append("hourly_alpaca_missing")
        if r["sources"]["daily_alpaca"]["status"] in (cfg.STATUS_PRELIMINARY, cfg.STATUS_MISSING):
            data_quality_flags.append("daily_alpaca_preliminary")

        rows.append(
            {
                "universe": r["universe"],
                "benchmark": r["benchmark"],
                "backtest_start": backtest_summary.get("period_start"),
                "backtest_end": backtest_summary.get("period_end"),
                "backtest_top_k": backtest_summary.get("top_k"),
                "backtest_strategy_return": backtest_summary.get("strategy_return_net"),
                "backtest_benchmark_return": backtest_summary.get("benchmark_return"),
                "backtest_difference": backtest_summary.get("difference"),
                "backtest_sharpe": backtest_summary.get("sharpe_ratio"),
                "backtest_max_drawdown": backtest_summary.get("max_drawdown"),
                "backtest_trades": backtest_summary.get("number_of_trades"),
                "hourly_observations": r["sources"]["hourly_alpaca"]["observations"],
                "hourly_strategy_return": hourly_summary.get("portfolio_return"),
                "hourly_benchmark_return": hourly_summary.get("benchmark_return"),
                "hourly_difference": hourly_summary.get("difference"),
                "daily_start": daily_summary.get("period_start"),
                "daily_end": daily_summary.get("period_end"),
                "daily_observations": daily_summary.get("observations"),
                "daily_strategy_return": daily_summary.get("portfolio_return"),
                "daily_benchmark_return": daily_summary.get("benchmark_return"),
                "daily_difference": daily_summary.get("difference"),
                "daily_trades": order_summary.get("total_trades"),
                "daily_max_drawdown": daily_summary.get("max_drawdown"),
                "signal_most_frequent_top1": aggregate.get("most_frequent_top1"),
                "signal_average_probability": aggregate.get("average_probability_overall"),
                "signal_average_turnover": aggregate.get("average_turnover"),
                "data_quality_status": ";".join(data_quality_flags) if data_quality_flags else "ok",
                "notes": r["sources"]["backtest"]["note"] if r["sources"]["backtest"]["status"] == cfg.STATUS_MISSING else "",
            }
        )

    comparison_df = pd.DataFrame(rows)
    csv_path = cfg.COMPARISON_OUTPUT_DIR / "final_universe_comparison.csv"
    comparison_df.to_csv(csv_path, index=False)
    print(f"Saved: {csv_path}")

    return comparison_df


