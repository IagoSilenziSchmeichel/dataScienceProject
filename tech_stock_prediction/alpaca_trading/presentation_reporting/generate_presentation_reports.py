def _interpret_backtest(r):
    summary = r["backtest_summary"]
    if summary is None:
        return f"Kein Backtest-Ergebnis vorhanden ({r['sources']['backtest']['note']})"
    direction = "schlaegt" if summary["difference"] > 0 else "unterliegt"
    return (
        f"Die Top-{summary['top_k']}-Strategie erzielt {summary['strategy_return_net']:+.1%} "
        f"gegenueber {summary['benchmark_return']:+.1%} fuer {r['benchmark']} "
        f"({summary['period_start'].date()} bis {summary['period_end'].date()}, {summary['number_of_days']} Handelstage). "
        f"Die Strategie {direction} den Benchmark um {summary['difference']:+.1%} "
        f"(Sharpe {summary['sharpe_ratio']:.2f}, Max Drawdown {summary['max_drawdown']:.1%}, "
        f"{summary['number_of_trades']} Trades, keine Transaktionskosten modelliert)."
    )


def _interpret_top_k(r):
    summary = r["top_k_summary"]
    if summary is None:
        return f"Keine Top-K-Ergebnisse vorhanden ({r['sources']['top_k']['note']})"
    return (
        f"Von Top-1 bis Top-5 performt Top-{summary['best_top_k']} am besten "
        f"({summary['best_strategy_return']:+.1%} Strategie-Rendite)."
    )


def _interpret_hourly_alpaca(r):
    summary = r["hourly_alpaca_summary"]
    if summary is None:
        return f"Keine Hourly-Paper-Trading-Auswertung verfuegbar ({r['sources']['hourly_alpaca']['note']})"
    if summary.get("portfolio_return") is None:
        return (
            f"Fuer Hourly liegen {summary.get('observations', 0)} Signal-/Order-Zeitpunkte vor, "
            "aber keine vollstaendige Portfolio-Zeitreihe mit mindestens zwei Portfoliowerten. "
            "Deshalb wird keine Rendite ausgewiesen."
        )
    direction = "vor" if summary["difference"] > 0 else "hinter"
    if r.get("hourly_is_reconstructed"):
        return (
            f"Die Hourly-Auswertung nutzt {summary['observations']} Handelsstunden "
            f"({summary['period_start']} bis {summary['period_end']}), rekonstruiert aus dem echten "
            f"Daily-Ergebnis desselben Universums (identisches Startkapital, identisches Endkapital, "
            f"identische Rendite). Die Strategie liegt mit {summary['portfolio_return']:+.1%} {direction} "
            f"{r['benchmark']} ({summary['benchmark_return']:+.1%}); Differenz {summary['difference']:+.1%}."
        )
    return (
        f"Die stundenbasierte Top-{summary['top_k']}-Auswertung nutzt {summary['observations']} Stunden "
        f"({summary['period_start']} bis {summary['period_end']}). Die Strategie liegt mit "
        f"{summary['strategy_return']:+.1%} {direction} {r['benchmark']} ({summary['benchmark_return']:+.1%}); "
        f"Differenz {summary['difference']:+.1%}, Sharpe {summary['sharpe_ratio']:.2f}, "
        f"Max Drawdown {summary['max_drawdown']:.1%}."
    )


def _interpret_daily_alpaca(r):
    summary = r["daily_alpaca_summary"]
    if summary is None:
        return f"Keine Daily-Paper-Trading-Daten verfuegbar ({r['sources']['daily_alpaca']['note']})"
    direction = "vor" if summary["difference"] > 0 else "hinter"
    return (
        f"Die isolierte Daily-Auswertung nutzt {summary['observations']} Handelstage "
        f"({summary['period_start'].date()} bis {summary['period_end'].date()}). Das Portfolio liegt mit "
        f"{summary['portfolio_return']:+.1%} {direction} {r['benchmark']} ({summary['benchmark_return']:+.1%}); "
        f"Differenz {summary['difference']:+.1%}, Trades {summary['number_of_trades']}."
    )


def write_presentation_results_summary(universe_results):
    lines = ["# Presentation Results Summary", ""]

    for r in universe_results:
        lines.append(f"## {r['title']}")
        lines.append("")
        lines.append("Forschungsfrage:")
        lines.append(r["research_question"])
        lines.append("")
        lines.append("Backtest:")
        lines.append(_interpret_backtest(r))
        lines.append("")
        lines.append("Top-K-Analyse:")
        lines.append(_interpret_top_k(r))
        lines.append("")
        lines.append("Daily Paper Trading:")
        lines.append(_interpret_daily_alpaca(r))
        lines.append("")
        lines.append("Hourly Paper Trading:")
        lines.append(_interpret_hourly_alpaca(r))
        lines.append("")

        core_claim = "Backtest-Ergebnis noch nicht verfuegbar."
        if r["backtest_summary"] is not None:
            summary = r["backtest_summary"]
            core_claim = (
                f"Im verfuegbaren Testzeitraum {'schlaegt' if summary['difference'] > 0 else 'schlaegt nicht'} "
                f"die Top-{summary['top_k']}-Outperformance-LSTM-Strategie {r['benchmark']} "
                f"({summary['difference']:+.1%})."
            )
        lines.append("Kernaussage:")
        lines.append(core_claim)
        lines.append("")

    lines.append("## Gesamtvergleich")
    lines.append("")

    with_backtest = [r for r in universe_results if r["backtest_summary"] is not None]
    if with_backtest:
        best_backtest = max(with_backtest, key=lambda r: r["backtest_summary"]["difference"])
        lowest_drawdown = min(with_backtest, key=lambda r: r["backtest_summary"]["max_drawdown"])
        lines.append(f"- Staerkstes Backtest-Universum: {best_backtest['title']} ({best_backtest['backtest_summary']['difference']:+.1%})")
        lines.append(f"- Geringster Max Drawdown (Backtest): {lowest_drawdown['title']} ({lowest_drawdown['backtest_summary']['max_drawdown']:.1%})")
    else:
        lines.append("- Kein Universum hat aktuell ein vollstaendiges Backtest-Ergebnis.")

    with_hourly = [
        r for r in universe_results
        if r["hourly_alpaca_summary"] is not None and r["hourly_alpaca_summary"].get("difference") is not None
    ]
    if with_hourly:
        best_hourly = max(with_hourly, key=lambda r: r["hourly_alpaca_summary"]["difference"])
        lines.append(f"- Staerkstes Hourly-Universum: {best_hourly['title']} ({best_hourly['hourly_alpaca_summary']['difference']:+.1%})")
    else:
        lines.append("- Staerkstes Hourly-Universum: nicht auswertbar (keine belastbare Hourly-Attribution verfuegbar).")

    with_daily = [r for r in universe_results if r["daily_alpaca_summary"] is not None]
    if with_daily:
        best_daily = max(with_daily, key=lambda r: r["daily_alpaca_summary"]["difference"])
        lines.append(f"- Staerkstes Daily-Universum: {best_daily['title']} ({best_daily['daily_alpaca_summary']['difference']:+.1%})")

    lines.append(
        "- Wichtigste wissenschaftliche Einschraenkung: Daily Paper Trading beruht auf wenigen Handelstagen; "
        "Hourly Paper Trading hat im Projektzeitraum noch keine vollstaendige Portfolio-Zeitreihe fuer alle "
        "Universen und ersetzt noch keinen laenger durchlaufenden Alpaca-Serverbetrieb."
    )
    lines.append("")

    output_path = cfg.OUTPUT_ROOT / "presentation_results_summary.md"
    output_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Saved: {output_path}")


