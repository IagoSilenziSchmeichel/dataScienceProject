def _archived_root(universe_name):
    return cfg.UNIVERSE_RESULTS_ROOT / universe_name


def _validate_ticker_set(data, universe_name, ticker_column="Ticker"):
    expected = set(cfg.UNIVERSES[universe_name])
    actual = set(data[ticker_column].unique())
    return actual == expected


def load_rf_dataset(universe_name):
    """
    Dataset section source: archived Random Forest data-prep output
    (raw download, chronological train/validation/test split). Only trusts
    the archived per-universe folder - never the shared live experiments/
    folder, which can belong to whichever universe ran last.
    """
    root = _archived_root(universe_name) / "exp_1_randomforest" / "data"
    raw_path = root / "raw" / "tech_stocks_raw.csv"
    train_path = root / "processed" / "train.csv"
    validation_path = root / "processed" / "validation.csv"
    test_path = root / "processed" / "test.csv"

    if not (raw_path.exists() and train_path.exists() and validation_path.exists() and test_path.exists()):
        return None

    raw = pd.read_csv(raw_path, parse_dates=["Date"])
    train = pd.read_csv(train_path, parse_dates=["Date"])
    validation = pd.read_csv(validation_path, parse_dates=["Date"])
    test = pd.read_csv(test_path, parse_dates=["Date"])

    if not _validate_ticker_set(train, universe_name):
        return None

    return {
        "raw_rows": int(len(raw)),
        "raw_start": raw["Date"].min(),
        "raw_end": raw["Date"].max(),
        "raw_tickers": sorted(raw["Ticker"].unique()),
        "train_rows": int(len(train)), "train_start": train["Date"].min(), "train_end": train["Date"].max(),
        "validation_rows": int(len(validation)), "validation_start": validation["Date"].min(), "validation_end": validation["Date"].max(),
        "test_rows": int(len(test)), "test_start": test["Date"].min(), "test_end": test["Date"].max(),
        "tickers": sorted(train["Ticker"].unique()),
        "feature_columns_in_data": [c for c in train.columns if c not in ("Date", "Ticker")],
    }


def load_standard_lstm(universe_name):
    """
    Standard-LSTM baseline (up/down prediction, no benchmark comparison) -
    only the files regenerated by every orchestrator run: 03_model_training
    (training history), 05_backtesting (lstm_standard_results.csv),
    10_top_k_backtest (lstm_standard_top_k_results.csv).
    """
    processed = _archived_root(universe_name) / "exp_2_lstm" / "data" / "processed"
    history_path = processed / "lstm_training_history.csv"
    results_path = processed / "lstm_standard_results.csv"
    top_k_path = processed / "lstm_standard_top_k_results.csv"

    if not (history_path.exists() and results_path.exists()):
        return None

    history = pd.read_csv(history_path)
    results = pd.read_csv(results_path).iloc[0]
    top_k = pd.read_csv(top_k_path) if top_k_path.exists() else None

    best_epoch = int(history.loc[history["validation_f1"].idxmax(), "epoch"])
    return {
        "epochs_trained": int(history["epoch"].max()),
        "best_validation_f1": float(history["validation_f1"].max()),
        "best_epoch": best_epoch,
        "final_train_loss": float(history["train_loss"].iloc[-1]),
        "results": results.to_dict(),
        "top_k_table": top_k,
    }


def load_outperformance_lstm(universe_name):
    """Reuses the exact same code path as generate_presentation_reports.py Plot 1/2."""
    backtest_ds = loader.load_backtest_predictions(universe_name)
    if not backtest_ds.is_usable:
        return None, backtest_ds

    best_top_k, candidates = metrics.select_best_top_k(backtest_ds.source_path)
    if best_top_k is None:
        best_top_k = max(
            candidates,
            key=lambda k: metrics.total_return_from_returns(
                metrics.reconstruct_daily_topk_backtest(backtest_ds.data, cfg.BENCHMARKS[universe_name], k)["Strategy_Return"]
            ),
        )
    benchmark_ticker = cfg.BENCHMARKS[universe_name]
    daily = metrics.reconstruct_daily_topk_backtest(backtest_ds.data, benchmark_ticker, best_top_k)
    summary = metrics.summarize_backtest(daily, benchmark_ticker)
    summary["top_k"] = best_top_k
    top_k_table = metrics.load_top_k_table(backtest_ds.source_path)

    results_row = None
    results_path = Path(backtest_ds.source_path).parent / "lstm_outperformance_results.csv"
    if results_path.exists():
        try:
            results_row = pd.read_csv(results_path).iloc[0].to_dict()
        except Exception:
            results_row = None

    return {
        "summary": summary,
        "top_k_table": top_k_table,
        "daily": daily,
        "predictions": backtest_ds.data,
        "source_path": backtest_ds.source_path,
        "results_row": results_row,
    }, backtest_ds


